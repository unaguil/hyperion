package graphcreation.collisionbased.sdg;

public class NonLocalServiceException extends Exception {

	/**
	 * 
	 */
	private static final long serialVersionUID = 1L;

	public NonLocalServiceException() {
	}

	public NonLocalServiceException(final String arg0) {
		super(arg0);
	}

	public NonLocalServiceException(final Throwable arg0) {
		super(arg0);
	}

	public NonLocalServiceException(final String arg0, final Throwable arg1) {
		super(arg0, arg1);
	}
}
